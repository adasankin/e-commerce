            e.preventDefault();
        });

        $('.review-rating .review-rating-stars').click(function() {
            var index = $(this).index();
            $(this).closest('.review-rating').find('.review-rating-stars').each(function(i, e) {
                if (i <= index) {
                    $(e).addClass('text-warning');
                } else {
                    $(e).removeClass('text-warning');
                }
            });
            $(this).parent().parent().find('input').val(index + 1);
        });
