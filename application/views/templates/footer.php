                onSuccess: function(result) {
                    swal({
                        title: "Payment Success!",
                        text: "Thank you for your payment!",
                        icon: "success",
                    });
                    setTimeout(function() {
                        location.href = "<?= base_url('transaction') ?>/" + result.order_id;
                    }, 1000);
                },
                onPending: function(result) {
                    swal({
                        title: "Payment Pending!",
                        text: "Your payment is pending!",
                        icon: "warning",
                    });
                    setTimeout(function() {
                        location.reload();
                    }, 1000);
                },
                onError: function(result) {
                    swal({
                        title: "Payment Failed!",
                        text: "Your payment is failed!",
                        icon: "error",
                    });
                    setTimeout(function() {
                        location.reload();
                    }, 1000);
                },
                onClose: function() {}
            });
        });

